# The primitive data types in Python are:
# Strings
# Integers
# Floats
# Booleans


# Subscripting
print("Hello"[0]) #H
#we can specify here negative as well as positive indices
print("Hello"[-1]) #o -1/4
print("Hello"[0:2]) #Slicing

# String Concatenation 
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers we can write as _
print(123_456_789) #same as 123456789

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)